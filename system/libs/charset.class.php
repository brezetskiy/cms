	* @version 5.0.1 ףסמגונרוםסעגמגאםמ מןנוהוכוםטו ךמהטנמגךט הכ אםדכטיסךמדמ עוךסעא חא סקוע סממעםמרוםט
	* ךמכטקוסעגא נףססךטץ בףךג ט כאעטםסךטץ. (ןמעמלף קעמ ןנט םאץמהוםטט סטלגמכמג " טח גמנהא טץ ןנוגנאשאכמ ג 
	* נףססךטו בףךג )
	* @param string $text
	* @return mixed
	*/
	static function detectCyrCharset($text) {
	    if (strlen($text) == 0) return false; // סכט ףבנאע עמ ג ךמםצו בףהוע הוכוםטו םא םמכ
	    
		if (extension_loaded('mbstring')) {
			return mb_detect_encoding($text, CMS_DETECTION_ORDER);
		}

		// וס םטזםודמ ט גונץםודמ נודטסענמג
		$lowercase = 3;
		$uppercase = 1;
		
	    $charsets = Array ( 
						'KOI8-R' => 0, 
						'CP1251' => 0, 
						'CP866' => 0, 
						'ISO-8859-1' => 0, 
						'MacCyrillic' => 0 
					); 
	    for ( $i = 0, $length = strlen($text); $i < $length; $i++ ) { 
	        $char = ord($text[$i]);
	        
	        //non-russian characters 
	        if ($char < 128 || $char > 256) continue; 
	         
	        //CP866 
	        if (($char > 159 && $char < 176) || ($char > 223 && $char < 242)) $charsets['CP866']+=$lowercase; 
	        if (($char > 127 && $char < 160)) $charsets['CP866']+=$uppercase; 
	         
	        //KOI8-R 
	        if (($char > 191 && $char < 223)) $charsets['KOI8-R']+=$lowercase; 
	        if (($char > 222 && $char < 256)) $charsets['KOI8-R']+=$uppercase; 
	         
	        //WIN-1251 
	        if ($char > 223 && $char < 256) $charsets['CP1251']+=$lowercase; 
	        if ($char > 191 && $char < 224) $charsets['CP1251']+=$uppercase; 
	         
	        //MAC 
	        if ($char > 221 && $char < 255) $charsets['MacCyrillic']+=$lowercase; 
	        if ($char > 127 && $char < 160) $charsets['MacCyrillic']+=$uppercase; 
	         
	        //ISO-8859-5 
	        if ($char > 207 && $char < 240) $charsets['ISO-8859-1']+=$lowercase; 
	        if ($char > 175 && $char < 208) $charsets['ISO-8859-1']+=$uppercase; 
	         
	    }
	    
	    // UTF-8
	    $russian_utf8 = '||||||||||||||||||||||||‗|||||||||¸|י|צ|ף|ך|ו|ם|ד|ר|ש|ח|ץ|ת|פ||ג|א|ן|נ|מ|כ|ה|ז|‎||ק|ס|ל|ט|ע||ב|‏';
		$russian_utf8 = @iconv('Windows-1251', 'UTF-8//IGNORE', $russian_utf8);
		$charsets['UTF-8'] = preg_match_all('/'.$russian_utf8.'/', $text, $matches) * 5;
		
	    arsort($charsets); 
	    $result = key($charsets);
	    
	    if (100 * $charsets[$result] / strlen($text) < 10) {
	    	// אהוםםו סטלגמכ סמסעאגכ‏ע לוםוו 10% עוךסעא, חםאקטע ‎עמ אםדכטיסךטי עוסע ס Word'a (ס פטדףנםלט ךאגקךאלט)
	    	return false;
	    } else {
	    	return $result;
	    }
	} 

	/**
	* וםוע ךמהטנמגךף סענאםטצ
	* @param string $source_charset
	* @param string $destination_charset
	* @param string $text
	* @return void
	*/
	static function convertCharSet($source_charset, $destination_charset, $text) {
		/**
		* ונוךמהטנףול עוךסע, וסכט םומבץמהטלמ
		*/
		if (!Charset::compareCharSets($destination_charset, $source_charset)) {
			// אנאלוענ IGNORE - ףהאכוע סטלגמכ, ךמעמנו מם םו למזוע חאלוםטע,
			// םמ WARNING גסו נאגםמ גמחםטךאוע, ןמ‎עמלף ל ודמ דאסטל
			// אןנטלונ ¹ ג אםדכטיסךמל עוךסעו גחגאוע WARNING
			$text = @iconv($source_charset, $destination_charset.'//IGNORE', $text);
		}
		return $text;
	}
	
	/**
	* גונוע סץמזוסע ךמהטנמגמך, ס ףקועמל טץ אכעונםאעטגםץ טלום
	* @param string $charset1
	* @param string $charset2
	* @return bool
	*/
	static function compareCharSets($charset1, $charset2) {

		$charset1 = strtolower($charset1);
		$charset2 = strtolower($charset2);
		
		if ($charset1 == $charset2) {
			return true;
		}
		
		/**
		* כעונםאעטגםו טלוםא ךמהטנמגמך
		* @var array
		*/
		$charset_alias = array(
			'iso-8859-5' => 'iso-8859-5',
			'iso_8859-5:1988' => 'iso-8859-5',
			'iso-ir-144' => 'iso-8859-5',
			'iso_8859-5' => 'iso-8859-5',
			'cyrillic' => 'iso-8859-5',
			'iso8859-5' => 'iso-8859-5',
			'iso-8859.5' => 'iso-8859-5',
			'iso8859.5' => 'iso-8859-5',
			'iso' => 'iso-8859-5',
			
			'ibm866' => 'ibm866',
			'csibm866' => 'ibm866',
			'866' => 'ibm866',
			'cp866' => 'ibm866',
			'x-cp866' => 'ibm866',
			'x-ibm866' => 'ibm866',
			'cp-866' => 'ibm866',
			'alt' => 'ibm866',
			'dos' => 'ibm866',
			
			'windows-1251' => 'windows-1251',
			'win' => 'windows-1251',
			'x-cp1251' => 'windows-1251',
			'cp1251' => 'windows-1251',
			'cp-1251' => 'windows-1251',
			
			'koi8-r' => 'koi8-r',
			'koi-8-r' => 'koi8-r',
			'koi8' => 'koi8-r',
			'koi-8' => 'koi8-r',
			'koi' => 'koi8-r'
		);

		if (
			isset($charset_alias[$charset1])
			&& isset($charset_alias[$charset2])
			&& $charset_alias[$charset2] == $charset_alias[$charset2]
		) {
			return true;
		} else {
			return false;
		}
	}
	
