	/**
	* מחגנאשאוע לאססטג גסוץ גמחלמזםץ גאנטאםעמג ענאםסכטעונאצטי סכמגא, מסםמגאםםץ םא עאב. $tab_translit
	* @param string $word		
	* @return array
	*/
	static public function getTranslitAll($word) {
		$mass = array();
		$arr = str_split($word);
		Charset::findTranslitVariants($arr, sizeof($arr)-1, 0, $mass);
		return $mass;
	}
		
	/**
	* סןמלמדאעוכםא פףםךצט. וךףנסטגםמ ןונובטנאוע גסו גמחלמזםו ענאםסכטעונאצטט עוךסעא ט חאםמסטע טץ ג לאססטג $mass
	* @param string $val	
	* @param int $lenght	
	* @param int $current_ch	
	* @param array $mass
	*/
	static private function findTranslitVariants($val, $lenght, $current_ch=0, &$mass=array()) {
		$variants = Charset::$tab_translit[$val[$current_ch]];
		$next_ch = $current_ch + 1;
		for($j=0;$j<sizeof($variants);$j++){
			$val[$current_ch] = $variants[$j];
			if($current_ch < $lenght) Charset::findTranslitVariants($val, $lenght, $next_ch, $mass);
			else array_push($mass, implode($val));
		}	
	}
		
	/**
	* נמגונוע עוךסע םא םאכטקטו ףךנאטםסךטץ סטלגמכמג
	* @param string $val	
	* @return bool
	*/
	static public function isUAChars($val) {
		$ua_chars = array('' => '', '÷' => '', '¯' => '', '×' => ''); 
		$res = strtr($val,$ua_chars);
		
		if(strcmp($val, $res)){
			return true;
		}
		
		return false;
	}
		
	/**
	* נמגונוע עוךסע םא םאכטקטו נףססךטץ סטלגמכמג
	* @param string $val	
	* @return bool
	*/
	static public function isRUChars($val) {
		$ru_chars = array('ת' => '', '' => '', '‎' => '', '' => '', '' => '', '' => ''); 
		$res = strtr($val, $ru_chars);
		
		if(strcmp($val, $res)){
			return true;
		}
		
		return false;
	}
	
